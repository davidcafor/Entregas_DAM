    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                
        //COMPROBACIONES
        
        StringBuilder sb = new StringBuilder("Se han producidos los siguientes errores:\n");
        boolean formularioOK = true;
        
        if(txtDNI.getText().trim().isEmpty()){
            sb.append("- Falta indicar el DNI\n");
            formularioOK = false;
        }
        if(txtNombre.getText().trim().isEmpty()){
            sb.append("- Falta indicar el Nombre\n");
            formularioOK = false;
        }
        if(txtApel1.getText().trim().isEmpty()){
            sb.append("- Falta indicar el primer apellido\n");
            formularioOK = false;
        }
        if(txtApel2.getText().trim().isEmpty()){
            sb.append("- Falta indicar el segundo apellido\n");
            formularioOK = false;
        }
        if(txtTlf.getText().trim().isEmpty()){
            sb.append("- Falta indicar el telefono\n");
            formularioOK = false;
        }
        
        if(formularioOK == false){
            JOptionPane.showMessageDialog(this, sb);
            return;
        }
        
        
        //CONSULTA BASE DATOS de Inserccion de datos
        
        String consulta = "INSERT INTO CLIENTES VALUES (?,?,?,?,?)";
        
        try (PreparedStatement sentencia = Conexion.getCon().prepareStatement(consulta)){
            
            sentencia.setString(1, txtDNI.getText());
            sentencia.setString(2, txtNombre.getText());
            sentencia.setString(3, txtApel1.getText());
            sentencia.setString(4, txtApel2.getText());
            sentencia.setString(5, txtTlf.getText());
            
            sentencia.executeUpdate();
        
        } catch (SQLIntegrityConstraintViolationException e){ //EN EL PRIMER CATCH ME ASEGURO DE MOSTRAR AVISO DE CLAVE DUPLICADA AL USUARIO
            JOptionPane.showMessageDialog(this, "El DNI ya est√° registrado en la BD");
        } catch (SQLException ex) {
            Logger.getLogger(NuevoCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception e) {
            System.out.println("Error: " + e);
        } finally {
            //Si esta abierta la ventana de bajacliente o altafiesta recargo combos en tiempo real.
            ((FrmPPal)getOwner()).actualizarCombosAltaFiestaBajaCliente();
        }
        
        //LIMPIAR DATOS
        txtDNI.setText("");
        txtNombre.setText("");
        txtApel1.setText("");
        txtApel2.setText("");
        txtTlf.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

