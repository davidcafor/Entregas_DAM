    private void btnCerrarElemListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCerrarElemListaActionPerformed
        // TODO add your handling code here:
        List<JDialog> ventanas = lstVentanas.getSelectedValuesList();
        
        for (JDialog v : ventanas) {
            v.setVisible(false);
            listaVentanas.remove(v);            
            actualizarModelo();
        }
    }//GEN-LAST:event_btnCerrarElemListaActionPerformed

