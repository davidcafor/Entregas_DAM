    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:

        if (rdoMostrarTipoA.isSelected()) {
            for (JDialog v : listaVentanas) {
                if (v instanceof VentanaTipoA) {
                    v.setVisible(true);
                }
            }
        }
        
        if (rdoMostrarTipoB.isSelected()) {
            for (JDialog v : listaVentanas) {
                if (v instanceof VentanaTipoB) {
                    v.setVisible(true);
                }
            }
        }
        
        if (rdoMostrarTipoC.isSelected()) {
            for (JDialog v : listaVentanas) {
                if (v instanceof VentanaTipoC) {
                    v.setVisible(true);
                }
            }
        }
        
        if (rdoMostrarTodas.isSelected()) {
            for (JDialog v : listaVentanas) {
                v.setVisible(true);
            }
        }
        
        actualizarModelo();


    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnOcultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOcultarActionPerformed
        // TODO add your handling code here:
        if(rdoOcultarTipoA.isSelected()){
            for (JDialog v : listaVentanas) {
                if(v instanceof VentanaTipoA){
                    v.setVisible(false);
                }
            }
        }
        
        if(rdoOcultarTipoB.isSelected()){
            for (JDialog v : listaVentanas) {
                if(v instanceof VentanaTipoB){
                    v.setVisible(false);
                }
            }
        }
        if(rdoOcultarTipoC.isSelected()){
            for (JDialog v : listaVentanas) {
                if(v instanceof VentanaTipoC){
                    v.setVisible(false);
                }
            }
        }
        if(rdoOcultarTodas.isSelected()){
            for (JDialog v : listaVentanas) {
                    v.setVisible(false);
            }
        }
        
        actualizarModelo();
    }//GEN-LAST:event_btnOcultarActionPerformed

